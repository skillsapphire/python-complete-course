# Operators are special symbols that helps to perform operations on operands(variables and values)
a = 20
b = 5

# Add two operands
print('a + b =', a+b) # Output: a + b = 25

# Subtract right operand from the left
print('a - b =', a-b) # Output: a - b = 15

# Multiply two operands
print('a * b =', a*b) # Output: a * b = 100

# Divide left operand bb the right one
print('a / b =', a/b) # Output: a / b = 4.0

# Floor division (quotient)
print('a // b =', a//b) # Output: a // b = 4

# Remainder of the division of left operand bb the right
print('a % b =', a%b) # Output: a % b = 0

# Left operand raised to the power of right (a^b)
print('a ** b =', a**b) # Output: a ** b = 3200000

# These are the shorthand of above operations +=, -=, *=, %=, //= and **=
c = 2
c += 10 # This equivalant to c = 2 + 10
print(c) # Output will be 12